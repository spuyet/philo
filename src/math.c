float			ft_cos(float angle)
{
	int			i;
	float		dec;

	while (angle > 360.0f)
		angle -= 360.0f;
	while (angle < 0.0f)
		angle += 360.0f;
	i = angle;
	dec = angle - i;
	return ((1.0f - dec) * g_cos[i] + dec * g_cos[i + 1]);
}

float			ft_sin(float angle)
{
	return (ft_cos(angle - 90.0f));
}
